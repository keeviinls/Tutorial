package ArrayOperaciones;

public class MetodosEstaticos_OPERACION_ {
    public static int sumar (int x1, int x2){
        int resultado;
        resultado = x1 + x2;
        return resultado;
    }

    public static int restar (int x1, int x2){
        int resultado;
        resultado = x1 - x2;
        return resultado;
    }
}
